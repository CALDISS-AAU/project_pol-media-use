def mp_check2(text, date, mps = MPs, MPs_inC = MPs_inC):
    '''
    Checks MP mentions in article.
    '''
    
    if any(mp in text for mp in mps.keys()):
        name_matches = list(compress(mps, [mp in text for mp in mps.keys()]))
    else:
        name_matches = []
    
    name_matches_actual = []
    for name_match in name_matches:
        for synonym, name in mps.items():
            if synonym in name_match:
                name_match = name_match.replace(synonym, name)
                break
        name_matches_actual.append(name_match)
    
    name_matches_actual = list(set(name_matches_actual))
    
    # Check if MP was in parliament at time of article

    name_matches_checked = []
    for mp in name_matches_actual:
        mp_inC_dates = MPs_inC.get(mp)

        inC_start = datetime.strptime(mp_inC_dates.get('start'), '%Y-%m-%d')
        inC_end = datetime.strptime(mp_inC_dates.get('end'), '%Y-%m-%d')

        article_date = datetime.strptime(date, '%Y-%m-%d')

        if article_date >= inC_start and article_date <= inC_end:
            name_matches_checked.append(mp)

    return(name_matches_checked)